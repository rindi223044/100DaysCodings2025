public class Perbandingan {
    public static void main(String[] args) {
        int angka1 = 10;
        int angka2 = 20;

        // Operator <
        if (angka1 < angka2) {
            System.out.println("angka1 lebih kecil dari angka2");
        }

        // Operator >
        if (angka2 > angka1) {
            System.out.println("angka2 lebih besar dari angka1");
        }
    }
}
