public class LogikaOR {
    public static void main(String[] args) {
        int x = 5;
        int y = 10;

        // Menggunakan operator logika OR (||)
        if (x > 3 || y < 5) {
            System.out.println("Salah satu kondisi benar.");
        } else {
            System.out.println("Kedua kondisi salah.");
        }
    }
}
