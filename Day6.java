public class OperasiPecahan {
    public static void main(String[] args) {
        float a = 5.5f;
        float b = 2.0f;
        float hasilFloat = a / b;

        double x = 10.75;
        double y = 4.25;
        double hasilDouble = x * y;

        System.out.println("Hasil pembagian float  : " + hasilFloat);
        System.out.println("Hasil perkalian double : " + hasilDouble);
    }
}
