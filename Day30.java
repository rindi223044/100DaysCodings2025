public class PerbandinganAngka {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Operator lebih besar
        if (a > b) {
            System.out.println(a + " lebih besar dari " + b);
        }

        // Operator lebih kecil
        if (b < a) {
            System.out.println(b + " lebih kecil dari " + a);
        }
    }
}
