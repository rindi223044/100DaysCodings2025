public class Aritmatika {
    public static void main(String[] args) {
        // Deklarasi dan inisialisasi variabel
        int angka1 = 20;
        int angka2 = 4;

        // Operator perkalian
        int hasilPerkalian = angka1 * angka2;

        // Operator pembagian
        int hasilPembagian = angka1 / angka2;

        // Menampilkan hasil
        System.out.println("Hasil Perkalian: " + hasilPerkalian); // 20 * 4 = 80
        System.out.println("Hasil Pembagian: " + hasilPembagian); // 20 / 4 = 5
    }
}
